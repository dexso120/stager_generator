// Both slice will preserve its order
func InsertRandomly(slice1, slice2 []string) []string {
    rand.Seed(time.Now().UnixNano())

    totalLen := len(slice1) + len(slice2)
    result := make([]string, totalLen)

    // randomly pick positions for slice2 elements
    positions := rand.Perm(totalLen)[:len(slice2)]
    slices.Sort(positions)

    // place slice2 elements at chosen positions
    for i, pos := range positions {
        result[pos] = slice2[i]
    }

    // fill remaining positions with slice1 elements in order
    j := 0
    for i, val := range result {
        if val == "" && j < len(slice1) {
            result[i] = slice1[j]
            j++
        }
    }

    return result
}

