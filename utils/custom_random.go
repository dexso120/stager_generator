func InsertRandomComments(lines []string, wordListFile string, wordsPerComment, numComments int) ([]string, error) {
    // read and parse wordlist
    content, err := os.ReadFile(wordListFile)
    if err != nil {
        return nil, fmt.Errorf("[-] Failed to read wordlist: %w", err)
    }

    var words []string
    scanner := bufio.NewScanner(strings.NewReader(string(content)))
    for scanner.Scan() {
        if word := strings.TrimSpace(scanner.Text()); word != "" {
            words = append(words, word)
        }
    }

    if len(words) == 0 {
        return nil, fmt.Errorf("[-] Wordlist is empty")
    }

    // generate random comments
    comments := make([]string, numComments)
    for i := range comments {
        selected := make([]string, wordsPerComment)
        for j := range selected {
            selected[j] = words[rand.Intn(len(words))]
        }
        comments[i] = "// " + strings.Join(selected, " ")
    }

    // insert comments randomly into lines preserving order
    return InsertRandomly(lines, comments), nil
}

func RandRange(min, max int) int{
    if (max < min){
        fmt.Println("[-] RandRange range error: min > max.")
        return 0
    }
    return rand.Intn(max-min) + min
}


